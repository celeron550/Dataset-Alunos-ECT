def parse_equivalencias(equiv_str):
    """
    Converte string de equivalência em lista de grupos.
    OU separa grupos alternativos; E une dentro do grupo.
    Retorna [[cod, ...], [cod, ...], ...]
    """
    if not equiv_str or str(equiv_str).strip().upper() in ('', 'NAN', 'NONE'):
        return []
    grupos = []
    partes_ou = re.split(r'\s+OU\s+', str(equiv_str).strip(), flags=re.IGNORECASE)
    for parte in partes_ou:
        codigos = re.findall(r'[A-Z]{2,4}\d{3,4}', parte)
        if codigos:
            grupos.append(codigos)
    return grupos


def aluno_cumpriu(grupos_equiv, aprovados_set):
    """True se ao menos UM grupo de equivalência foi inteiramente aprovado."""
    for grupo in grupos_equiv:
        if all(cod in aprovados_set for cod in grupo):
            return True
    return False


