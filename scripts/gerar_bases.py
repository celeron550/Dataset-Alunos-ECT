# Encode do status geral do curso do discente
CURSO_MAP = {
    "CONCLUÍDO": 1,
    "FORMADO":   1,
    "ATIVO - FORMANDO": 0,
    "ATIVO":    0,
    "CANCELADO": -1,
}

def encode_descricao(s):
    if pd.isna(s):
        return 0
    s_upper = str(s).upper().strip()
    for key, val in DESCRICAO_MAP.items():
        if key in s_upper:
            return val
    return 0

def encode_curso(s):
    if pd.isna(s):
        return 0
    s_upper = str(s).upper().strip()
    for key, val in CURSO_MAP.items():
        if key in s_upper:
            return val
    return 0


